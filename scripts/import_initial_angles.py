def main():
    env = load_env()
    notion_token = env.get("NOTION_TOKEN")
    if not notion_token:
        print("Додай NOTION_TOKEN у .env")
        sys.exit(1)
    ids = load_db_ids()
    db_id = ids["content_angles"]
    for a in ANGLES:
        prefix = a["title"].split(":")[0]
        if page_exists(notion_token, db_id, prefix):
            print("Пропускаю, вже існує:", a["title"])
            continue
        create_angle_page(
            notion_token,
            db_id,
            a["title"],
            a["hook"],
            a["insight"],
            a["fact"],
            a["claim"],
        )
        print("OK:", a["title"])
    print("Готово: 7 кутів у базі «Контент-кути».")


if __name__ == "__main__":
    main()

