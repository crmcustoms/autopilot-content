ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if ROOT not in sys.path:
    sys.path.insert(0, ROOT)


def load_env():
    env_path = os.path.join(ROOT, ".env")
    if not os.path.exists(env_path):
        raise FileNotFoundError("Немає .env у корені проекту")
    env = {}
    with open(env_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, v = line.split("=", 1)
                env[k.strip()] = v.strip().strip('"').strip("'")
    return env


def load_db_ids():
    path = os.path.join(ROOT, "NOTION_DATABASE_IDS.json")
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def notion_request(token, method, path, body=None):
    url = "https://api.notion.com/v1" + path
    headers = {
        "Authorization": "Bearer " + token,
        "Notion-Version": "2022-06-28",
        "Content-Type": "application/json",
    }
    data = json.dumps(body, ensure_ascii=False).encode("utf-8") if body else None
    req = urllib.request.Request(url, data=data, headers=headers, method=method)
    try:
        with urllib.request.urlopen(req) as resp:
            return json.loads(resp.read().decode())
    except urllib.error.HTTPError as e:
        body_err = e.read().decode() if e.fp else ""
        raise RuntimeError(f"Notion {e.code}: {body_err}") from e


def page_exists(token, db_id, title_prefix):
    body = {
        "filter": {"property": "Назва", "title": {"starts_with": title_prefix}},
        "page_size": 1,
    }
    out = notion_request(token, "POST", "/databases/" + db_id + "/query", body)
    return bool(out.get("results"))


def create_angle_page(token, db_id, title, hook, insight, fact, claim):
    def rt(text):
        if not text:
            return []
        return [{"type": "text", "text": {"content": text}}]

    body = {
        "parent": {"database_id": db_id},
        "properties": {
            "Назва": {"title": [{"type": "text", "text": {"content": title[:2000]}}]},
            "Хук": {"rich_text": rt(hook)},
            "Інсайт": {"rich_text": rt(insight)},
            "Факт": {"rich_text": rt(fact)},
            "Твердження": {"rich_text": rt(claim)},
        },
    }
    return notion_request(token, "POST", "/pages", body)


